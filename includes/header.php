$(function () {

$('.nav-dropdown-toggle').dropdown();

$('.nav-dropdown-link').each(function () {

var target = $(this).data('dropdown');
$(this).on('mouseenter focus', function () {

if (window.innerWidth > 1199) {
if (!$(target + '-container').hasClass('show')) {
$(target).dropdown('toggle');
}
}
});
$('#navbar-dropdown-backdrop').on('mouseenter focus', function () {
$('.dropdown-menu.navbar-dropdown-menu').each(function () {
if ($(this).hasClass('show')) {
var target = '#' + $(this).attr('id').replace('-container', '');

$(target).dropdown('toggle');
}
});
});

$(target).parent().on('show.bs.dropdown', function () {
$('#navbar-dropdown-backdrop').addClass('show');
});
$(target).parent().on('hide.bs.dropdown', function () {
$('#navbar-dropdown-backdrop').removeClass('show');
});


if ($.fn.dropdown.Constructor.VERSION.charAt(0) == '3') {
$(target).parent().on('show.bs.dropdown', function () {
$(target + '-container').addClass('show');
$('#navbar-dropdown-backdrop').addClass('show');
});
$(target).parent().on('hide.bs.dropdown', function () {
$(target + '-container').removeClass('show');
$('#navbar-dropdown-backdrop').removeClass('show');
});
}
});

if ($.fn.dropdown.Constructor.VERSION.charAt(0) == '3') {
var navbarCollapse = $('#navbarCollapse')
navbarCollapse.on('shown.bs.collapse', function () {
navbarCollapse.addClass('show');
});
navbarCollapse.on('hidden.bs.collapse', function () {
navbarCollapse.removeClass('show');
});
}

});
